export const cities = Object.keys(cityHospitalsData);

export const getSyncedHospitals = () => {
    if (typeof window === "undefined") return cityHospitalsData;

    try {
        const stored = localStorage.getItem("docfind_hospitals");
        if (!stored) return cityHospitalsData;

        const localData = JSON.parse(stored);
        const merged = { ...cityHospitalsData };

        Object.keys(cityHospitalsData).forEach((city) => {
            if (!localData[city]) return;

            const masterHospitals = cityHospitalsData[city].hospitals;
            const localHospitalsMap = new Map((localData[city].hospitals || []).map(h => [h.id, h]));

            const mergedHospitals = masterHospitals.map(masterH => {
                const localH = localHospitalsMap.get(masterH.id);
                if (localH) {
                    return {
                        ...masterH,
                        name: localH.name || masterH.name,
                        beds: localH.beds || masterH.beds,
                    };
                }
                return masterH;
            });

            merged[city].hospitals = mergedHospitals;
        });

        return merged;

    } catch (e) {
        return cityHospitalsData;
    }
};
